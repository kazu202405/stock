-- =============================================
-- 紛らわしいテーマに定義を入れる
--
-- 背景:
--   タグ名だけをLLMに渡すと、意味が広い語に判定が引き寄せられた。
--   実測では「専門商社」が7銘柄中4件に付き、メーカー（キユーピー・TSI）にも
--   誤って付与された。厨房機器メーカーに「建設」が付く例もあった。
--
--   名前だけでは境界が伝わらないため、誤りやすいものに一文の定義を添える。
--   定義はプロンプトに含めて判定させる。
-- =============================================

UPDATE stock_tags SET description = v.description
FROM (VALUES
  -- 商社系（最も誤りが多い）
  ('専門商社', '他社の製品を仕入れて販売する卸売業。自社で製造している企業には付けない'),
  ('総合商社', '幅広い商材を扱い事業投資も行う大手商社。専門分野に特化した企業には付けない'),

  -- 業種の大分類（下位テーマがある場合はそちらを優先させる）
  ('機械', '産業用機械・装置のメーカー。工作機械や建設機械など下位のテーマが該当する場合はそちらを使う'),
  ('電気機器', '電機・電子機器のメーカー。半導体や電子部品など下位のテーマが該当する場合はそちらを使う'),
  ('化学', '化学品のメーカー。塗料・ファインケミカルなど下位が該当する場合はそちらを使う'),
  ('建設', '建物や土木構造物を施工する企業。設備を納入するメーカーには付けない'),
  ('ゼネコン', '大規模な建築・土木を元請けする総合建設会社'),
  ('食品', '食品・調味料のメーカー。小売や外食を営む企業には付けない'),

  -- 小売・サービス（「売り先」と混同されやすい）
  ('食品スーパー', '食品を販売する小売店舗を運営する企業。食品メーカーには付けない'),
  ('外食', '飲食店を運営する企業。食材や厨房設備を供給する企業には付けない'),
  ('eコマース', '自社でネット通販サイトを運営する企業'),
  ('人材派遣', '人材を派遣する事業を営む企業。自社で採用しているだけの企業には付けない'),
  ('広告', '広告の制作・代理・配信を事業とする企業。自社商品を宣伝しているだけの企業には付けない'),

  -- 金融（階層が近く取り違えやすい）
  ('メガバンク', '三菱UFJ・三井住友・みずほなど巨大金融グループ。地方銀行やネット銀行には付けない'),
  ('銀行', '銀行業を営む企業'),
  ('地方銀行', '特定の地域を地盤とする銀行'),
  ('フィンテック', '金融と技術を組み合わせたサービスを提供する企業。従来型の銀行業務のみの企業には付けない'),

  -- テーマ性が強く過剰付与されやすいもの
  ('人工知能', '人工知能そのものを開発・提供する企業。業務に利用しているだけの企業には付けない'),
  ('生成AI', '生成AIの開発・提供を事業とする企業'),
  ('データセンター', 'データセンターの運営、または建設・電源・冷却設備を供給する企業'),
  ('半導体', '半導体そのものを設計・製造する企業。製造装置や部材は下位のテーマを使う'),
  ('ロボット', 'ロボット本体またはその主要部品のメーカー'),
  ('医療機器', '医療機器のメーカー。医薬品の企業には付けない')
) AS v(name, description)
WHERE stock_tags.name = v.name;
