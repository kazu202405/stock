-- =============================================
-- 銘柄タグ（事業テーマ／属性）
--
-- 目的:
--   「半導体で探す」「核融合で探す」のような探索軸を持たせる。
--
-- 設計上の判断:
--   1. テーマ名は市場で実際に使われている呼称に揃える。
--      自己流の名前を付けると、ユーザーが検索する言葉と一致しない。
--      （例: 「AI半導体」でなく「半導体」、「ヒューマノイド」でなく「ヒト型ロボット」、
--            「暗号資産」でなく「仮想通貨」、「GLP-1」でなく「肥満症治療薬」）
--
--   2. kind でテーマと属性を分ける。
--      テーマ（半導体・iPS細胞）は「探す」軸。
--      属性（中国関連・親子上場・TOPIXコア30）は「絞る／除外する」軸で、
--      単独では母集団が広すぎて機能しないが、数値条件と組み合わせると効く。
--
--   3. tagging_enabled と display_active を分ける。
--      鮮度が落ちたテーマ（NFT・メタバース等）もタグ付けはしておき、画面には出さない。
--      タグを付ける限界費用はほぼゼロだが、後から足すと全銘柄の再判定
--      （約2.5時間＋API費用）が必要になるため、この非対称性に備える。
-- =============================================

CREATE TABLE IF NOT EXISTS stock_tags (
    name            VARCHAR(80) PRIMARY KEY,
    kind            VARCHAR(16) NOT NULL DEFAULT 'theme',  -- theme / attribute
    category        VARCHAR(40),
    description     TEXT,

    tagging_enabled BOOLEAN DEFAULT TRUE,   -- LLMのタグ付け候補に含めるか
    display_active  BOOLEAN DEFAULT TRUE,   -- 画面の一覧に出すか

    sort_order      INTEGER DEFAULT 100,
    created_at      TIMESTAMPTZ DEFAULT NOW(),
    updated_at      TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS stock_tag_map (
    company_code VARCHAR(10) NOT NULL,
    tag_name     VARCHAR(80) NOT NULL,
    source       VARCHAR(16) DEFAULT 'llm',   -- llm / manual / rule
    created_at   TIMESTAMPTZ DEFAULT NOW(),
    PRIMARY KEY (company_code, tag_name)
);

CREATE INDEX IF NOT EXISTS idx_tag_map_tag  ON stock_tag_map(tag_name);
CREATE INDEX IF NOT EXISTS idx_tag_map_code ON stock_tag_map(company_code);
CREATE INDEX IF NOT EXISTS idx_stock_tags_kind ON stock_tags(kind, display_active);

COMMENT ON TABLE  stock_tags                 IS '事業テーマ・属性のマスタ';
COMMENT ON COLUMN stock_tags.kind            IS 'theme=探す軸 / attribute=絞る軸';
COMMENT ON COLUMN stock_tags.tagging_enabled IS 'LLMのタグ付け候補に含めるか';
COMMENT ON COLUMN stock_tags.display_active  IS '画面の一覧に出すか。休眠テーマはfalse';

ALTER TABLE stock_tags    ENABLE ROW LEVEL SECURITY;
ALTER TABLE stock_tag_map ENABLE ROW LEVEL SECURITY;


